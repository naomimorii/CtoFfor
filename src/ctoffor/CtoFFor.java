public class CtoFFor {


    public static void main(String[] args) {
    int c;
    double f;
    
    for (c=10; c<110; c=c+10)
     {
      f = (9*c + 160)/5;
      System.out.print( c + " - " + f + " \n ");
     }
    }
    
}
